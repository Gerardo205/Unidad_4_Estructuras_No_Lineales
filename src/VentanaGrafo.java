    private void btnMAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMAActionPerformed
        boolean datos[][] = g.matrizAdyacencia();
        String m = "     ";
        for (int i = 0; i < cv; i++) {
            m = m + cmbVertices.getItemAt(i) + "  ";
        }
        for (int i = 0; i < cv; i++) {
            m = m + "\n" + cmbVertices.getItemAt(i) + "  ";
            for (int j = 0; j < cv; j++) {
                m += datos[i][j] ? "1 " : "0 ";
            }
        }
        txtMostrar.setText(m);
    }//GEN-LAST:event_btnMAActionPerformed

    private void btnLAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLAActionPerformed
        if (cv > 0)
            txtMostrar.setText(g.listaAdy((char)cmbVertices.getSelectedItem()));
    }//GEN-LAST:event_btnLAActionPerformed

