    private void btnIVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIVActionPerformed
        char dato = txtDato.getText().toUpperCase().charAt(0);
            g.insertarVertice(dato);
            cmbOrigen.addItem(dato);
            cmbDestino.addItem(dato);
            cmbVertices.addItem(dato);
            cv++;
            setTitle("Se insertó correctamente el vertice " + dato);
    }//GEN-LAST:event_btnIVActionPerformed

    private void btnEVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEVActionPerformed
        char dato = (char) cmbVertices.getSelectedItem();
            cmbOrigen.removeItem(dato);
            cmbDestino.removeItem(dato);
            cmbVertices.removeItem(dato);
            setTitle("Se eliminó correctamente el vertice " + dato);
    }//GEN-LAST:event_btnEVActionPerformed

